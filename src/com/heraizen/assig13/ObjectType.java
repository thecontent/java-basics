package com.heraizen.assig13;

public class ObjectType {
	
	String ename;
	String email;
	String mobile;
	int age;
	double salary;
	int id;

	public ObjectType(int id, String ename, String mobile, int salary, int age, String email) {
		this.id = id;
		this.ename = ename;
		this.email = email;
		this.mobile = mobile;
		this.age = age;
		this.salary = salary;
	}

}
